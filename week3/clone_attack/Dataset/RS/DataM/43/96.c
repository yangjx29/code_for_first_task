int isprime (int n) {
    int i;
    {
        i = 453 - 451;
        while (n >= i * i) {
            if (n % i == (455 - 455))
                return 0;
            i = i + 1;
        };
    }
    return 1;
}

int main (int argc, char *argv []) {
    int m, j;
    scanf ("%d", &m);
    {
        j = 3;
        while (j * (155 - 153) <= m) {
            if (isprime (j) && isprime (m - j))
                printf ("%d %d\n", j, (m - j));
            j += 2;
        };
    }
    return 0;
}

